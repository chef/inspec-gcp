title 'Test GCP google_logging_folder_exclusions resource.'

folder_exclusion = input('folder_exclusion', value: {
  "name"=>"inspec-folder-exclusion",
  "description"=>"My folder exclusion description",
  "filter"=>"resource.type = gce_instance AND severity <= DEBUG"
})
gcp_organization_id = input(:gcp_organization_id, value: '', description: 'The identifier of the organization that is the parent of the folder')
gcp_enable_privileged_resources = input(:gcp_enable_privileged_resources, value: 0, description: 'Flag to enable privileged resources requiring elevated privileges in GCP.')

control 'google_logging_folder_exclusions-1.0' do
  impact 1.0
  title 'google_logging_folder_exclusions resource test'

  only_if { gcp_enable_privileged_resources.to_i == 1 && gcp_organization_id != '' }
  # Getting folder exclusions is complicated due to the name being generated by the server.
  # This can be drastically simplified if you have the name when writing the test
  describe.one do
    google_resourcemanager_folders(parent: "organizations/#{gcp_organization_id}").names.each do |name|
      # name on a folder is in the form `folders/12345`
      describe google_logging_folder_exclusions(folder: name.split('/')[1]) do
        its('names'){ should include folder_exclusion['name'] }
        its('descriptions'){ should include folder_exclusion['description'] }
        its('filters'){ should include folder_exclusion['filter'] }
      end
    end
  end
end
